
public class Variables {
	public static void main(String[] Args) 
	{
	//Variable Type : Whole Number
		int Num1;
		Num1 = 100;
	//Variable Type : Decimal 	
		double d;	//default
		d = 5.25;
		float f = 3.16f;	//f is mandatory for float as it is not default
	//Variable Type : Character
		char c = 'H';
		
	//Variable Type String
		String name = "Harsha";
		
	//Variable Type = Boolean	Only True OR False
		boolean b = false;

	//Boolean Expression 
		int x = 100;
		int y = 200;
		
		System.out.println("Int Num : "+Num1+"\nDouble d : "+d+"\nFloat : "+f+"\nChar c : "+c+"\nString Name : "+name+"\nboolean b : "+b);
		System.out.print("Boolean Expression : ");
		System.out.println(x>y);
		//System.out.println("HEllo");
		
	}	
}
